// Wap for swapping three variables. 
#include <iostream>
using namespace std;
int main(){
    int a,b,temp;
    cout<<"Enter the Number a: ";
    cin>>a;
    cout<<"Enter the Number b: ";
    cin>>b;


    temp = a;
    a = b;
    b = temp;

    cout<<"Here is the Value of a: "<<a<<endl;
    cout<<"Here is the value of b: "<<b;




}