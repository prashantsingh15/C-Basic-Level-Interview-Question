// Wap for the use of % mode.

#include <iostream>
using namespace std;
int main() {
    int num;

    cout<<"Enter the Number: ";
    cin>>num;

    

    if(num % 2 == 0){
        cout<<"The Given Number is even Number... "<<num;

    }

   
    else{
        cout<<"The given number is odd... "<<num;

    }

    
}